export enum FormFieldResponsiveSize {
  /**
   * Specifies the responsive size of the form field:
   * - On small (S) screens, the form field will be medium (M) size.
   * - On medium (M) screens, the form field will be medium (M) size.
   * - On large (L) screens, the form field will be large (L) size.
   */
  MML = 'MML',

  /**
   * Specifies the responsive size of the form field:
   * - On small (S) screens, the form field will be medium (M) size.
   * - On medium (M) screens, the form field will be large (L) size.
   * - On large (L) screens, the form field will be large (L) size.
   */
  MLL = 'MLL'
}
